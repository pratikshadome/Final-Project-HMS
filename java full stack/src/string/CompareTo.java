package string;

public class CompareTo {

	public static void main(String[] args)
	{
		String s1 = "Pratiksha";
		String s2 = "Prajakta";
		String s3 = "Pratiksha";
		
		System.out.println(s1.compareTo(s2));
		System.out.println(s2.compareTo(s1));
		System.out.println(s1.compareTo(s3));
		
	}

}
